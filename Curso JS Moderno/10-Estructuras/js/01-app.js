// Estructuras de control - creando un IF

// Muchas veces queremos que nuestro codigo se ejecute en orden.
// Esctructuras de control -> si se cumple una condicion ejecutamos el comando. Ejemplo cajero de un banco

const puntaje = 1000;

// Dentro del if ponemos la condicion que queremos revisar.

if (puntaje == 1000) {
  // Si se esta cumpliendo con la condicion ejecuta el codigo
  console.log("Si es igual..");
} else {
  // En caso de que no se ejecute la primera condicion que se ejecute la segunda
  console.log("No es igual");
}
