// Diferencia entre FUNCIONES y METODOS

// Aunque los metoso y la funciones son muy parecidos, la forma en que son nombrados metodo y funcion tiene que ver con el contexto en donde son utilizadas.

const numero1 = 20;
const numero2 = "20";

console.log(parseInt(numero2)); // Esto es una funcion por su sitaxis. -> nombre seguido de un ().

console.log(numero1.toString()); // Esto es un metodo; nombre de la variable seguido de un punto.
