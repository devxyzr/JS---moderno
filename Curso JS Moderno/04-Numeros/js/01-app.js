// Creacion de numeros en JS -> Todos los numeros en JS se crean de la misma manera: no es necesario especificar un tipo de dato diferente a numero.

const numero1 = 20; // Numero entero
const numero2 = "20"; //Valor numero en string
const numero3 = 20.2; //Valor numero float
const numero4 = 0.10203;
const numero5 = -20; //Numero negativo
const numero6 = new Number(80); //Numero como objeto

console.log(numero1);
console.log(numero2);
console.log(numero3);
console.log(numero4);
console.log(numero5);
console.log(numero6);
